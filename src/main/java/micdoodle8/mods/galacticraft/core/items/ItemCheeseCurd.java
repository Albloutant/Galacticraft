public class ItemCheeseCurd extends ItemFood
{
	public ItemCheeseCurd(int par2, float par3, boolean par4)
	{
		super(par2, par3, par4);
	}

	public ItemCheeseCurd(int par2, boolean par3)
	{
		this(par2, 0.6F, par3);
	}

	@Override
	@SideOnly(Side.CLIENT)
	public void registerIcons(IIconRegister iconRegister)
	{
		this.itemIcon = iconRegister.registerIcon(GalacticraftCore.ASSET_PREFIX + "cheese_curd");
	}

	@Override
	public CreativeTabs getCreativeTab()
	{
		return GalacticraftCore.galacticraftTab;
	}

	@Override
	@SideOnly(Side.CLIENT)
	public EnumRarity getRarity(ItemStack par1ItemStack)
	{
		return ClientProxy.galacticraftItem;
	}
}
